"""
hw_test_generator.py — Hardware Unit Test Scaffold Generator for KiCad AI Copilot
==================================================================================
Reads a design JSON and generates a complete firmware test scaffold including
pin definitions, HAL stubs, Unity test harness, hardware manifest, and CMakeLists.

Usage:
    python hw_test_generator.py --input design.json --framework stm32 --output ./firmware/
"""

import argparse
import json
import os
import sys
from datetime import datetime
from typing import Any

SUPPORTED_FRAMEWORKS = ("stm32", "arduino", "generic")

BANNER = f"// Generated by KiCad AI Copilot hw_test_generator.py on {{ts}}\n// DO NOT EDIT — regenerate from design.json\n"

# ---------------------------------------------------------------------------
# Helper: component type → peripheral category
# ---------------------------------------------------------------------------

def _classify(comp: dict) -> str:
    t = comp.get("type", "").upper()
    name = comp.get("name", "").upper()
    combined = t + " " + name
    if any(k in combined for k in ("MCU", "STM32", "ATMEGA", "MICROCONTROLLER")):
        return "MCU"
    if "LED" in combined:
        return "LED"
    if "SPI" in combined or "FLASH" in combined:
        return "SPI"
    if "I2C" in combined or "EEPROM" in combined or "IMU" in combined:
        return "I2C"
    if "BUTTON" in combined or "BTN" in combined or "SWITCH" in combined:
        return "BUTTON"
    if "POWER" in combined or "LDO" in combined or "VREG" in combined or "REG" in combined:
        return "POWER"
    if "UART" in combined or "SERIAL" in combined:
        return "UART"
    if "PWM" in combined or "MOTOR" in combined:
        return "PWM"
    return "GENERIC"


def _safe_name(label: str) -> str:
    """Convert an arbitrary string to a valid C identifier."""
    return "".join(c if c.isalnum() or c == "_" else "_" for c in label).upper()


