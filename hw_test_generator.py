def _gen_pins_h(components: list[dict], framework: str) -> str:
    ts = datetime.now().isoformat(timespec="seconds")
    lines = [
        BANNER.format(ts=ts),
        "#pragma once",
        "#ifndef PINS_H",
        "#define PINS_H",
        "",
        "// ── Pin / Net Definitions ─────────────────────────────────────────",
    ]

    if framework == "stm32":
        gpio_prefix = "P"
        ports = "ABCDEFGH"
        _port_idx = 0
        _pin_idx  = 0

        def next_pin() -> str:
            nonlocal _port_idx, _pin_idx
            pin = f"{gpio_prefix}{ports[_port_idx % len(ports)]}{_pin_idx}"
            _pin_idx += 1
            if _pin_idx > 15:
                _pin_idx = 0
                _port_idx += 1
            return pin

    elif framework == "arduino":
        _pin_counter = [2]

        def next_pin() -> str:  # type: ignore[misc]
            p = str(_pin_counter[0])
            _pin_counter[0] += 1
            return p
    else:
        _pin_counter = [0]

        def next_pin() -> str:  # type: ignore[misc]
            p = f"GPIO_{_pin_counter[0]}"
            _pin_counter[0] += 1
            return p

    for comp in components:
        ref   = _safe_name(comp.get("ref", comp.get("id", "UNKNOWN")))
        ctype = _classify(comp)
        nets  = comp.get("nets", {})
        lines.append(f"")
        lines.append(f"// {comp.get('ref', comp.get('id', '?'))} — {ctype}")
        if nets:
            for net_name, net_val in nets.items():
                macro = f"PIN_{ref}_{_safe_name(net_name)}"
                value = net_val if net_val else next_pin()
                lines.append(f"#define {macro:<40s} {value}")
        else:
            # Auto-assign generic pins
            for pin_label in _default_pins_for(ctype):
                macro = f"PIN_{ref}_{_safe_name(pin_label)}"
                lines.append(f"#define {macro:<40s} {next_pin()}")

    lines += ["", "#endif // PINS_H", ""]
    return "\n".join(lines)


def _default_pins_for(ctype: str) -> list[str]:
    return {
        "MCU":     ["RESET", "BOOT0"],
        "LED":     ["ANODE"],
        "SPI":     ["CS", "SCK", "MOSI", "MISO"],
        "I2C":     ["SDA", "SCL"],
        "BUTTON":  ["IN"],
        "POWER":   ["EN", "PG"],
        "UART":    ["TX", "RX"],
        "PWM":     ["OUT"],
        "GENERIC": ["IO"],
    }.get(ctype, ["IO"])


def _gen_hal_stubs_h(components: list[dict]) -> str:
    ts = datetime.now().isoformat(timespec="seconds")
    seen: set[str] = set()
    protos: list[str] = []

    for comp in components:
        ctype = _classify(comp)
        if ctype in seen:
            continue
        seen.add(ctype)
        protos += _hal_protos(ctype)

    lines = [
        BANNER.format(ts=ts),
        "#pragma once",
        "#ifndef HAL_STUBS_H",
        "#define HAL_STUBS_H",
        "",
        "#include <stdint.h>",
        "",
        "// ── HAL Stub Declarations ─────────────────────────────────────────",
        "",
    ]
    for p in protos:
        lines.append(f"{p};")
    lines += ["", "#endif // HAL_STUBS_H", ""]
    return "\n".join(lines)


def _hal_protos(ctype: str) -> list[str]:
    return {
        "MCU":     ["void MCU_Init(void)", "void MCU_Reset(void)"],
        "LED":     ["void LED_Set(uint8_t state)", "void LED_Toggle(void)"],
        "SPI":     ["void SPI_Init(void)",
                    "int  SPI_Transfer(uint8_t *tx, uint8_t *rx, uint16_t len)"],
        "I2C":     ["void I2C_Init(void)",
                    "int  I2C_Write(uint8_t addr, uint8_t *data, uint16_t len)",
                    "int  I2C_Read(uint8_t addr, uint8_t *data, uint16_t len)"],
        "BUTTON":  ["uint8_t BUTTON_Read(void)"],
        "POWER":   ["void    POWER_Init(void)", "float   POWER_GetVoltage(void)"],
        "UART":    ["void UART_Init(uint32_t baud)",
                    "void UART_Send(uint8_t *data, uint16_t len)"],
        "PWM":     ["void PWM_Init(void)", "void PWM_SetDuty(uint8_t duty_pct)"],
        "GENERIC": ["void GENERIC_Init(void)"],
    }.get(ctype, ["void GENERIC_Init(void)"])


def _gen_hal_stubs_c(components: list[dict]) -> str:
    ts = datetime.now().isoformat(timespec="seconds")
    seen: set[str] = set()
    impls: list[str] = []

    for comp in components:
        ctype = _classify(comp)
        if ctype in seen:
            continue
        seen.add(ctype)
        impls += _hal_impls(ctype)

    lines = [
        BANNER.format(ts=ts),
        '#include "hal_stubs.h"',
        '#include "pins.h"',
        "",
        "// ── HAL Stub Implementations ──────────────────────────────────────",
        "",
    ]
    lines += impls
    return "\n".join(lines)


