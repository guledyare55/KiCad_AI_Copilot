if HAS_KICAD:
    class KiCadAICopilotAction(pcbnew.ActionPlugin):
        """
        KiCad Action Plugin for AI Copilot.
        Appears in: Tools → External Plugins → KiCad AI Copilot
        """

        def defaults(self):
            self.name = "KiCad AI Copilot"
            self.category = "EDA Automation"
            self.description = (
                "Import AI-generated PCB designs from the KiCad AI Copilot web tool. "
                "Automatically places components, declares nets, and adds PCB placement rules."
            )
            self.show_toolbar_button = True

            icon = str(ICON_PATH)
            if os.path.exists(icon):
                self.icon_file_name = icon
            else:
                self.icon_file_name = ""

        def Run(self):
            """Called when the plugin is activated from KiCad Tools menu."""
            board = pcbnew.GetBoard()
            parent = wx.FindWindowByName("PcbFrame") or None

            # Show import dialog
            dlg = CopilotImportDialog(parent)
            result = dlg.ShowModal()
            options = dlg.get_options()
            dlg.Destroy()

            if result != wx.ID_OK:
                return

            json_path = options.get("json_path")
            if not json_path:
                wx.MessageBox(
                    "No JSON file selected.\n\n"
                    "Please generate a design using the KiCad AI Copilot web tool first, "
                    "then export the JSON.",
                    "No File", wx.OK | wx.ICON_WARNING
                )
                return

            # Load JSON
            try:
                with open(json_path, "r", encoding="utf-8") as f:
                    design = json.load(f)
            except Exception as e:
                wx.MessageBox(f"Failed to read JSON:\n{e}", "Error", wx.OK | wx.ICON_ERROR)
                return

            # Run importer
            importer = PCBImporter(board, design)
            success, log = importer.run(options)

            # Show results
            log_text = "\n".join(log[:30])
            if len(log) > 30:
                log_text += f"\n... and {len(log)-30} more"

            if success:
                comp_count = len(design.get("components", []))
                auto_count = len(design.get("auto_added_components", []))
                net_count = len(design.get("nets", []))
                wx.MessageBox(
                    f"✅ Design imported successfully!\n\n"
                    f"• {comp_count} components placed\n"
                    f"• {auto_count} auto-added support components referenced\n"
                    f"• {net_count} nets declared\n"
                    f"• Placement notes added\n\n"
                    f"Log:\n{log_text}\n\n"
                    f"⚠️  Note: Support components (decoupling caps, pull-ups, etc.) "
                    f"are listed in the BOM but you must manually add them to the schematic "
                    f"and PCB using the reference list.",
                    "Import Complete", wx.OK | wx.ICON_INFORMATION
                )
            else:
                wx.MessageBox(
                    f"Import completed with errors:\n\n{log_text}",
                    "Import Warnings", wx.OK | wx.ICON_WARNING
                )
