"""
KiCad AI Copilot — Action Plugin (KiCad 7, 8, 9)
==================================================
Uses the pcbnew ActionPlugin API (SWIG-based).
Note: SWIG pcbnew is deprecated in KiCad 9 and will be removed in KiCad 11.
For KiCad 10+, see plugin_ipc.py instead.

This plugin:
  1. Shows a dialog to browse for the AI Copilot JSON file
  2. Imports components as footprint stubs into the active PCB
  3. Adds net declarations and placement notes
  4. Opens the web UI if requested
"""

import os
import sys
import json
import webbrowser
from pathlib import Path

try:
    import pcbnew
    import wx
    HAS_KICAD = True
except ImportError:
    HAS_KICAD = False
    print("[KiCad AI Copilot] pcbnew not available — standalone mode")


# ── Constants ──────────────────────────────────────────────
PLUGIN_DIR = Path(__file__).parent
WEB_UI_PATH = PLUGIN_DIR.parent / "index.html"
ICON_PATH = PLUGIN_DIR / "resources" / "icon.png"

# KiCad unit conversion: 1mm = 1e6 internal units (nm-based in older KiCad)
# KiCad 7+: uses mm directly in most API calls
MM = 1_000_000  # pcbnew.FromMM(1)


